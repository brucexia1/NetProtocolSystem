static void test_des()
{
    unsigned char cbc_in[40];
    unsigned char cbc_out[40];
    DES_cblock iv3;
    DES_key_schedule ks;
    const size_t cbc_data_len = strlen((char *)cbc_data);

    DES_set_key(&cbc_key, &ks);
    memset(cbc_out, 0, sizeof(cbc_out));
    memset(cbc_in, 0, sizeof(cbc_in));
    memcpy(iv3, cbc_iv, sizeof(cbc_iv));
    DES_ncbc_encrypt(cbc_data, cbc_out, cbc_data_len + 1, &ks,
                     &iv3, DES_ENCRYPT);
    if (memcmp(cbc_out, cbc_ok, 32)) {
        printf("DES-CBC test 1 Fail.\n");
    } else
        printf("DES-CBC test 1 OK.\n");

    memcpy(iv3, cbc_iv, sizeof(cbc_iv));
    DES_ncbc_encrypt(cbc_out, cbc_in, cbc_data_len + 1, &ks,
                     &iv3, DES_DECRYPT);
    if (memcmp(cbc_in, cbc_data, cbc_data_len)) {
        printf("DES-CBC test 2 Fail.\n");
    } else
        printf("DES-CBC test 2 OK.\n");
}

static void test_3des()
{
    DES_cblock iv3;
    DES_key_schedule ks, ks2, ks3;
    unsigned char cbc_in[40];
    unsigned char cbc_out[40];
    const size_t i = strlen((char *)cbc_data) + 1;
    const size_t n = (i + 7) / 8 * 8;

    DES_set_key(&cbc_key, &ks);
    DES_set_key(&cbc2_key, &ks2);
    DES_set_key(&cbc3_key, &ks3);

    memset(cbc_out, 0, sizeof(cbc_out));
    memset(cbc_in, 0, sizeof(cbc_in));
    memcpy(iv3, cbc_iv, sizeof(cbc_iv));

    DES_ede3_cbc_encrypt(cbc_data, cbc_out, 16L, &ks, &ks2, &ks3, &iv3,
                         DES_ENCRYPT);
    DES_ede3_cbc_encrypt(&cbc_data[16], &cbc_out[16], i - 16, &ks, &ks2,
                         &ks3, &iv3, DES_ENCRYPT);
    if (memcmp(cbc_out, cbc3_ok, n)) {
        printf("3DES-CBC test 1 Fail.\n");
    } else
        printf("3DES-CBC test 1 OK.\n");

    memcpy(iv3, cbc_iv, sizeof(cbc_iv));
    DES_ede3_cbc_encrypt(cbc_out, cbc_in, i, &ks, &ks2, &ks3, &iv3,
                         DES_DECRYPT);
    if (memcmp(cbc_in, cbc_data, i)) {
        printf("3DES-CBC test 2 Fail.\n");
    } else
        printf("3DES-CBC test 2 OK.\n");

}

